import androidx.annotation.IntRange
import androidx.compose.ui.graphics.Color
import androidx.core.graphics.toColorInt

/**
 * Converts a hex string to a Compose Color.
 */
fun colorFromHex(hexColor: String): Color {
    val legacyColor = hexColor.toColorInt()
    return Color(legacyColor)
}

/**
 * Base Colors
 */
val White = Color.White
val Black= Color.Black
val Transparent = Color.Transparent

/**
 * Convenience extension for applying opacity
 */
infix fun Color.at(@IntRange(0, 100) percent: Int): Color {
    val value = (percent / 100f).coerceIn(0f, 1f)
    return copy(alpha = value)
}

